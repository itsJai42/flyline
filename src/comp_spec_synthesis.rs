// clap parser
// ──────────────────────────────────────────────────────────────────────────────

/// Parse clap-style `--help` output.
///
/// Typical structure:
/// ```text
/// <description>
///
/// Usage: <name> [OPTIONS] [COMMAND]
///
/// Commands:
///   subcommand1  Short description
///   subcommand2  Short description
///
/// Arguments:
///   [ARG]  description
///
/// Options:
///       --flag <VALUE>
///           Long description
///   -h, --help
///           Print help
/// ```
pub fn parse_help_clap(help: &str) -> Command {
    let lines: Vec<&str> = help.lines().collect();
    let mut cmd = Command::default();

    // Extract name from "Usage:" line; handles both "Usage: name ..." (clap/commander)
    // and "Usage:\n  name ..." (cobra/docopt) styles.
    if let Some(usage_pos) = lines
        .iter()
        .position(|l| l.trim_start().starts_with("Usage:"))
    {
        let after = lines[usage_pos].trim_start_matches(|c: char| c.is_whitespace());
        let after = after.strip_prefix("Usage:").unwrap_or(after).trim();
        if let Some(name_part) = after.split_whitespace().next() {
            // Standard "Usage: name ..." form.
            cmd.name = Some(name_part.to_string());
        } else {
            // Cobra/docopt form: "Usage:" is alone on its own line; name is on the next.
            for next_line in &lines[usage_pos + 1..] {
                let t = next_line.trim();
                if !t.is_empty() {
                    if let Some(name_part) = t.split_whitespace().next() {
                        cmd.name = Some(name_part.to_string());
                    }
                    break;
                }
            }
        }
    }

    // First non-empty line before "Usage:" is the description.
    let usage_pos = lines
        .iter()
        .position(|l| l.trim_start().starts_with("Usage:"))
        .unwrap_or(0);
    for line in &lines[..usage_pos] {
        let t = line.trim();
        if !t.is_empty() {
            cmd.description = Some(t.to_string());
            break;
        }
    }

    // Section parsing.
    let mut i = 0;
    while i < lines.len() {
        let trimmed = lines[i].trim();

        // ── Commands / Subcommands section ──────────────────────────────────
        if trimmed == "Commands:" || trimmed == "Subcommands:" || trimmed == "Available Commands:" {
            i += 1;
            while i < lines.len() {
                let line = lines[i];
                if line.trim().is_empty() {
                    i += 1;
                    break;
                }
                // Guard: a non-indented non-empty line is a new section header.
                if indent_of(line) == 0 && !line.trim().is_empty() {
                    break;
                }
                let trimmed_line = line.trim();
                // Format: "  subname  Short description"
                let mut parts = trimmed_line.splitn(2, "  ");
                let sub_name = parts.next().unwrap_or("").trim().to_string();
                let sub_desc = parts.next().map(|s| s.trim().to_string());
                if !sub_name.is_empty() {
                    cmd.subcommands.push(Command {
                        name: Some(sub_name),
                        description: sub_desc,
                        ..Command::default()
                    });
                }
                i += 1;
            }
            continue;
        }

        // ── Options / Arguments / Flags section ────────────────────────────
        if trimmed == "Options:"
            || trimmed == "Arguments:"
            || trimmed == "Flags:"
            || trimmed == "Global Flags:"
            || trimmed == "Global Options:"
            || trimmed == "Options are:"
        {
            i += 1;
            while i < lines.len() {
                let line = lines[i];
                if line.trim().is_empty() {
                    i += 1;
                    // blank line may separate args but we continue
                    continue;
                }
                // A non-indented non-empty line is a new section header.
                if indent_of(line) == 0 {
                    break;
                }
                let flag_indent = indent_of(line);
                let flag_part = line.trim();

                // Skip pure description continuation lines that are deeper
                // (they belong to the previous arg).
                // We identify flag lines as ones that start with '-' or '['.
                if !flag_part.starts_with('-') && !flag_part.starts_with('[') {
                    i += 1;
                    continue;
                }

                // Only parse tokens up to the first double-space to avoid
                // treating description text as flag tokens.
                let token_part = flag_part
                    .find("  ")
                    .map(|pos| &flag_part[..pos])
                    .unwrap_or(flag_part);
                let (short, long, value_type) = parse_flag_tokens(token_part);

                // Capture inline description (text after first double-space on
                // the flag line), then append any deeper-indented continuation.
                let inline_desc: Option<String> = flag_part.find("  ").and_then(|pos| {
                    let d = flag_part[pos..].trim();
                    if d.is_empty() {
                        None
                    } else {
                        Some(d.to_string())
                    }
                });
                let (cont_desc, next_i) = collect_continuation(&lines, i + 1, flag_indent);
                i = next_i;

                let description = match (inline_desc, cont_desc.as_str()) {
                    (Some(d), "") => Some(d),
                    (None, d) if !d.is_empty() => Some(d.to_string()),
                    (Some(d), rest) => Some(format!("{} {}", d, rest)),
                    (None, _) => None,
                };

                let num_args = if value_type.is_some() {
                    Some("1".to_string())
                } else {
                    None
                };

                cmd.args.push(Arg {
                    short,
                    long,
                    description,
                    value_type,
                    num_args,
                });
            }
            continue;
        }

        i += 1;
    }

    cmd
}

